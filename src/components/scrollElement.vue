<script setup lang="ts">
import { onMounted, ref, nextTick } from 'vue'
import gsap from 'gsap'
import ScrollTrigger from 'gsap/ScrollTrigger'
import ProjectCompIndex from '@/components/ProjectCompIndex.vue'
import Arrow from '@/components/icons/Arrow.vue'

gsap.registerPlugin(ScrollTrigger)

const projects = ref([
  {
    id: 1,
    speed: 0.8,
    xPercent: 30,
    yPercent: 10,
    imageSrc: '/img/Accueilimg1.webp',
    imageAlt: 'EYE ON ME',
    title: 'EYE ON ME',
    subtitle: 'VIEW PROJECT >',
  },
  {
    id: 2,
    speed: 0.9,
    xPercent: 70,
    yPercent: 45,
    imageSrc: '/img/Accueilimg2.webp',
    imageAlt: 'BRANDING PROJECT',
    title: 'BRANDING PROJECT',
    subtitle: 'VIEW PROJECT >',
  },
  {
    id: 3,
    speed: 1.0,
    xPercent: 30,
    yPercent: 70,
    imageSrc: '/img/Accueilimg3.webp',
    imageAlt: 'WEB PROJECT',
    title: 'WEB PROJECT',
    subtitle: 'VIEW PROJECT >',
  },
  {
    id: 4,
    speed: 1.1,
    xPercent: 60,
    yPercent: 95,
    imageSrc: '/img/Accueilimg2.webp',
    imageAlt: 'FULL DESIGN',
    title: 'FULL DESIGN',
    subtitle: 'VIEW PROJECT >',
  },
  {
    id: 5,
    speed: 1.1,
    xPercent: 5,
    yPercent: 95,
    imageSrc: '/img/Accueilimg2.webp',
    imageAlt: 'FULL DESIGN',
    title: 'FULL DESIGN',
    subtitle: 'VIEW PROJECT >',
  },
  {
    id: 6,
    speed: 1.1,
    xPercent: 5,
    yPercent: 95,
    imageSrc: '/img/Accueilimg2.webp',
    imageAlt: 'FULL DESIGN',
    title: 'FULL DESIGN',
    subtitle: 'VIEW PROJECT >',
  },
])

onMounted(() => {
  gsap.registerPlugin(ScrollTrigger)

  // ⚡ Attendre que la page ait bien chargé
  setTimeout(() => {
    ScrollTrigger.refresh()
  }, 200)

  ScrollTrigger.getAll().forEach((t) => t.kill()) // Supprime tous les ScrollTrigger actifs
  ScrollTrigger.refresh() // Recharge proprement les animations

  const mm = gsap.matchMedia()

  mm.add('(max-width: 640px)', () => {
    console.log('📱 Mobile mode')
    projects.value = projects.value.map((p, i) => ({
      ...p,
      xPercent: [3, 18, 3, 18, 3, 18][i],
      yPercent: [0, 16, 33, 49, 67, 85][i],
    }))
    updateAnimations()
  })

  mm.add('(min-width: 641px) and (max-width: 1023px)', () => {
    console.log('📲 Tablet 2 mode')
    projects.value = projects.value.map((p, i) => ({
      ...p,
      xPercent: [10, 40, 15, 38, 10, 38][i],
      yPercent: [0, 10, 25, 38, 52, 65][i],
    }))
    updateAnimations()
  })

  mm.add('(min-width: 1024px) and (max-width: 1280px)', () => {
    console.log('🖥️ Tbalet 2 mode')
    projects.value = projects.value.map((p, i) => ({
      ...p,
      xPercent: [10, 40, 8, 35, 15, 42][i],
      yPercent: [0, 19, 35, 45, 62, 75][i],
    }))
    updateAnimations()
  })

  mm.add('(min-width: 1281px)', () => {
    console.log('🖥️ Desktop 2 mode')
    projects.value = projects.value.map((p, i) => ({
      ...p,
      xPercent: [0, 25, 7, 32, 2, 28][i],
      yPercent: [0, 10, 30, 45, 60, 75][i],
    }))
    updateAnimations()
  })

  async function updateAnimations() {
    await nextTick() // Assure que Vue applique les nouvelles valeurs avant GSAP
    console.log('📌 New project values:', projects.value)

    // Rafraîchir GSAP AVANT de définir les animations
    ScrollTrigger.refresh()

    // Effet parallaxe sur les images
    document.querySelectorAll('.parallax-project').forEach((el, i) => {
      const project = el as HTMLElement

      const xInitial = projects.value[i].xPercent
      const yInitial = projects.value[i].yPercent

      // Mise à jour GSAP
      gsap.set(project, {
        x: `${xInitial}vw`,
        y: `${yInitial}vh`,
      })

      gsap.to(project, {


        ease: 'none',
        scrollTrigger: {
          trigger: project,
          start: 'top bottom',
          end: 'bottom top',
          scrub: true,
        },
      })
    })

    ScrollTrigger.refresh() // 🔄 Forcer GSAP à recalculer les animations
  }

  // 🟢 Fixer le h3 et le p jusqu'à la fin des projets
  const textPin = ScrollTrigger.create({
    trigger: '.intro-text',
    start: 'top+=160vh center', // ⚡ Meilleur positionnement
    end: `+=${window.innerHeight * 2.7}px`, // ⚡ Fixe plus longtemps
    pin: true,
    pinSpacing: false,
    scrub: 1, // ⚡ Meilleure fluidité
  })

  // 🟢 Fixer le texte après le passage des images
  ScrollTrigger.create({
    start: 'bottom top',
    onEnterBack: () => textPin.enable(),
    onLeave: () => textPin.disable(),
  })

  // 🎯 Réapparition du titre lorsqu'on remonte la page
  gsap.to('.text', {
    opacity: 1,
    scrollTrigger: {
      trigger: '.images',
      start: 'top top',
      end: 'top+=5px top',
      scrub: true,
    },
  })

  // 🟢 Rendre le bouton invisible tant que les images sont visibles
  // 🟢 Cacher le bouton dès que la première image arrive à l'écran
  gsap.fromTo(
    '.project-button',
    { opacity: 1 },
    {
      opacity: 0, // Le bouton disparaît
      scrollTrigger: {
        trigger: '.parallax-project:first-child', // Première image
        start: 'top 80%', // Dès qu'elle commence à apparaître
        end: 'top 50%', // Le bouton reste invisible
        scrub: true,
      },
    },
  )

  // 🟢 Faire réapparaître le bouton quand la dernière image quitte l'écran
  gsap.fromTo(
    '.project-button',
    { opacity: 0 },
    {
      opacity: 1, // Le bouton réapparaît
      scrollTrigger: {
        trigger: '.parallax-project:last-child', // Dernière image
        start: 'bottom 100%', // Lorsqu'elle approche de la sortie
        end: 'bottom 0%', // Le bouton devient totalement visible
        scrub: true,
      },
    },
  )
})

const scrollToTop = () => {
  window.scrollTo({ top: 0, behavior: 'auto' })
}
</script>

<template>
  <div
    class="overflow-hidden mt-[18rem] pb-[30rem] md:mt-[30rem] md:pb-[85rem] lg:pb-[45rem] lg:mt-[20rem] lg:mb-[10rem] xl:mt-[35rem] xl:pb-[55rem] xl:mb-[25rem] -mx-5"
  >
    <!-- Bloc du titre + description qui sera fixé -->
    <div class="intro-text">
      <h3
        class="text-[10vw] md:text-[7.5vw] font-lactos leading-[1.1] text-center text-coloblue lg:text-[7vw] xl:text-[6vw]"
      >
        YOUNG
        <span class="text-colored">
          <br />
          WEB DESIGNER
        </span>
      </h3>

      <p
        class="font-unbounded font-light text-xs md:text-base md:w-2/3 md:m-auto lg:text-lg lg:w-8/12 xl:text-2xl xl:w-7/12 text-center mx-5"
      >
        I bring a unique perspective to every project, blending expertise in
        <span class="font-medium">web design</span>,
        <span class="font-medium">motion design</span> with a passion for creativity. I’m committed
        to creating meaningful, impactful designs. <br />
        Let’s collaborate <span class="font-medium">to bring your vision to life</span>.
      </p>
      <routerLink to="/project" @click="scrollToTop">
        <button
          class="text-xs md:text-sm xl:text-lg project-button border-colored border text-colored p-1 px-3 md:p-2 md:px-4 lg:p-3 lg:px-5 rounded-[12rem] hover:text-coloblue hover:border-coloblue transition-colors duration-500 flex space-x-4 items-center mx-auto mt-3 lg:mt-6"
        >
          <p class="font-unbounded">MY PROJECTS</p>
          <Arrow class="w-3 md:w-4 xl:w-5 -rotate-90" />
        </button>
      </routerLink>

      <div class="buffer-space"></div>
    </div>

    <!-- ✅ Espace supplémentaire pour éviter que le texte soit coupé -->

    <!-- Conteneur des projets -->
    <div class="relative w-full max-w-[1200px] mx-auto min-h-[150vh]">
      <ProjectCompIndex
        v-for="project in projects"
        :key="project.id"
        :id="project.id"
        :imageSrc="project.imageSrc"
        :imageAlt="project.imageAlt"
        :title="project.title"
        :subtitle="project.subtitle"
        class="absolute project-item parallax-project w-7/12 md:w-4/12 lg:w-3/12 xl:w-4/12 text-sm"
        :data-speed="project.speed"
        :style="{
          left: `${project.xPercent}vw`,
